package Challenge.ifChallenge;

public class Main {
    public static void main(String[] args) {
        int a = 0;
        if (a > 0) {
            System.out.println("Liczba dodatnia większa od zera");
        } else if (a < 0) {
            System.out.println("Liczba ujemna mniejsza od zera");
        } else
            System.out.println("Liczba równa zero");
    }
}
